package ex_02_JavaPrograms;

public class Lab005_Common_Math {
    public static void main(String[] args) {
        System.out.println(2/2);
        System.out.println(2*2);
        System.out.println(2+2);
        System.out.println(2-2);
        System.out.println("2+2"); // In this case, when a value is provided within " " it is considered as a String and it will print the same value given inside the " "
        System.out.println(3/2); // In this case, only integer value will be printed as println only prints integer no decimal part
    }
}
